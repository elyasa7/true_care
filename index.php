<script>
	$(document).on('click', '.update_status', function (e) {
		e.preventDefault();
		var new_status = $(this).find('.status_id').text();

		var item_id = $(this).find('.item_id').text();
		var target_row = $('.item_block_' + item_id);

		$.ajax({
			url: "/function/change_status.php",
			data: {
				"agent_id": item_id,
				"status_id": new_status
			},
			type: "post",
			success: function (result) {
				$('.item_row_' + item_id).replaceWith(result);
				$(document).foundation();
			}
		});
		});
</script>

